/*
 * Generate pixel-perfect circle DOT sprites — one PNG per score bucket (+ highlighted).
 *
 * WHY pre-baked PNGs (not a text glyph or SDF): the old dot was a `●` text glyph whose
 * RECTANGULAR collision box was ~2.4× the visible dot and taller-than-wide, so dots culled
 * far apart and unevenly (diagonal worse than orthogonal). SDF would tint per-color but
 * scales fuzzy. A pre-baked PNG per bucket color, rendered 1:1 at @3x with icon-size 1, is
 * crisp (no upscale) and its collision box is a tight SQUARE ≈ the dot — uniform spacing.
 *
 * Colors come from apps/mobile/src/constants/score-bucket-palette.json (defaultRgb, ten deciles)
 * so dots match their pins exactly. Output: apps/mobile/src/assets/dots/dot-b<0..9>.png + dot-highlighted.png.
 *
 * Run: node scripts/generate-dot-sprites.js
 */
const path = require('path');
const fs = require('fs');
const sharp = require('sharp');

// Canonical Crave Score color tiers (0-10 scale, ten deciles) — same file the pin
// generator and quality-color.ts read, so dots match their pins exactly. Do not
// hardcode tier colors here.
const palette = require('../apps/mobile/src/constants/score-bucket-palette.json');

const OUT_DIR = path.join(__dirname, '..', 'apps', 'mobile', 'src', 'assets', 'dots');

// Visible dot diameter in points (≈ the old glyph's visible size). The PNG is rendered at
// DENSITY× this so Mapbox never upscales (icon-size 1, registered at scale = DENSITY).
const DOT_DIAMETER_PT = 8;
const DENSITY = 3; // @3x only (matches the pin pipeline; modern iPhones are @3x)
const PX = DOT_DIAMETER_PT * DENSITY; // 24px sprite
// 1px AA inset so the circle edge isn't clipped by the canvas bounds.
const RADIUS = PX / 2 - 1;

// TEN decile colors straight from score-bucket-palette.json (defaultRgb) + the
// highlighted PRIMARY_COLOR. Bucket i = palette.defaultRgb[i], one dot per tier.
const BUCKET_RGB = Object.fromEntries(palette.defaultRgb.map((rgb, i) => [i, rgb]));
const HIGHLIGHTED_RGB = [255, 51, 104]; // PRIMARY_COLOR #ff3368

const circleSvg = ([r, g, b]) =>
  `<svg width="${PX}" height="${PX}" xmlns="http://www.w3.org/2000/svg">` +
  `<circle cx="${PX / 2}" cy="${PX / 2}" r="${RADIUS}" fill="rgb(${r},${g},${b})"/>` +
  `</svg>`;

async function writeDot(name, rgb) {
  const out = path.join(OUT_DIR, `${name}.png`);
  await sharp(Buffer.from(circleSvg(rgb)), { density: 72 })
    .resize(PX, PX) // exact pixel size — crisp, no upscale
    .png()
    .toFile(out);
  return out;
}

const GENERATED_MODULE = path.join(
  __dirname,
  '..',
  'apps',
  'mobile',
  'src',
  'generated',
  'dot-images.ts'
);

function writeGeneratedModule(ids) {
  const imports = ids
    .map((id) => `import ${id.replace(/-/g, '_')} from '../assets/dots/${id}.png';`)
    .join('\n');
  const entries = ids.map((id) => `  '${id}': ${id.replace(/-/g, '_')},`).join('\n');
  const body =
    `// AUTO-GENERATED by scripts/generate-dot-sprites.js — do not edit.\n` +
    `// Maps Mapbox image id (e.g. "dot-b7") → bundled circle-dot sprite. One pre-baked PNG per\n` +
    `// score bucket (+ highlighted), 24px @3x → scale:3 renders at 8pt with icon-size:1 (crisp).\n` +
    `import type { ImageSourcePropType } from 'react-native';\n\n` +
    `${imports}\n\n` +
    `export const DOT_SPRITE_SCALE = ${DENSITY};\n\n` +
    `export const DOT_IMAGES: Record<string, ImageSourcePropType> = {\n${entries}\n};\n`;
  fs.mkdirSync(path.dirname(GENERATED_MODULE), { recursive: true });
  fs.writeFileSync(GENERATED_MODULE, body);
}

async function main() {
  fs.mkdirSync(OUT_DIR, { recursive: true });
  const written = [];
  const ids = [];
  for (const [bucket, rgb] of Object.entries(BUCKET_RGB)) {
    ids.push(`dot-b${bucket}`);
    written.push(await writeDot(`dot-b${bucket}`, rgb));
  }
  ids.push('dot-highlighted');
  written.push(await writeDot('dot-highlighted', HIGHLIGHTED_RGB));
  writeGeneratedModule(ids);
  console.log(`Generated ${written.length} dot sprites (${PX}px @${DENSITY}x) in ${OUT_DIR}`);
  written.forEach((w) => console.log('  ' + path.relative(path.join(__dirname, '..'), w)));
  console.log('  ' + path.relative(path.join(__dirname, '..'), GENERATED_MODULE));
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
